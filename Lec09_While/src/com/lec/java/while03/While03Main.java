package com.lec.java.while03;

public class While03Main {

	public static void main(String[] args) {
		System.out.println("while 연습");
		
		// while 사용 1~10 홀수 출력
		
		int count = 1;
		while(count <= 10) {
			System.out.println(count);
			count += 2;
		}
		

	} // end main()

} // end class While03Main









